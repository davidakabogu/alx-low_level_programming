#include "main.h"

/**
 * add - this function sums two integers
 * @a: int a
 * @b: int b
 * Return: returns the sum of both integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - this function subtracts two integers
 * @a: int a
 * @b: int b
 * Return: returns the difference of both integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - this function multiplies two integers
 * @a: int a
 * @b: int b
 * Return: returns the product of both integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - this function divides two integers
 * @a: int a
 * @b: int b
 * Return: returns the division of both integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - this function divides and returns the remainder of two integers
 * @a: int a
 * @b: int b
 * Return: returns the remainder of both integers
 */
int mod(int a, int b)
{
	return (a % b);
}
